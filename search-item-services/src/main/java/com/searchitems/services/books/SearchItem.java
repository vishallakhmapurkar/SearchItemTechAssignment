package com.searchitems.services.books;

public class SearchItem {
	private String kind;
	private String title;
	private String authors_artists;

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthors_artists() {
		return authors_artists;
	}

	public void setAuthors_artists(String authors_artists) {
		this.authors_artists = authors_artists;
	}

}
